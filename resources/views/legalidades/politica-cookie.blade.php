                    <h3>¿Qué son las Cookies y para qué se utilizan?</h3>
                    <p>Las cookies son archivos de texto generados por un servidor web y almacenados en el dispositivo del usuario (ordenador, smartphone, tablet) cuando accede a ciertas páginas web. Su finalidad es almacenar y recuperar información sobre la navegación del usuario.</p>
                    <p>El uso de cookies permite obtener información sobre la navegación de los usuarios, mejorar su experiencia, recopilar estadísticas de uso y personalizar contenidos según el comportamiento del usuario en el sitio web.</p>
                </section>
    
                <section class="cookie-subsection">
                    <h3>¿Cómo se autoriza el uso de las cookies en nuestro sitio web?</h3>
                    <p>El usuario autoriza y consiente el uso de cookies de la siguiente manera:</p>
                    <ol class="cookie-list">
                        <li>Aceptando el uso de cookies mediante el aviso de "ACEPTO" que aparece en la primera capa de información.</li>
                    </ol>
                    <p>A pesar de la aceptación inicial, el usuario puede revocar su consentimiento en cualquier momento deshabilitando las cookies en la configuración de su navegador.</p>
